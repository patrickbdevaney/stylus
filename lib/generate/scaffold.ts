function slugify(name: string): string {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "")
    .slice(0, 40);
}

function googleFontHref(tokens: BrandTokens): string | null {
  const families = Array.from(
    new Set(tokens.fonts.map((font) => font.family)),
  ).filter(
    (family) => !/courier|georgia|times|system-ui/i.test(family),
  );
  if (families.length === 0) return null;

  const params = families
    .map((family) => {
      const font = tokens.fonts.find((entry) => entry.family === family);
      const weights = font?.weights.join(";") ?? "400;700";
      return `family=${encodeURIComponent(family)}:wght@${weights}`;
    })
    .join("&");

  return `https://fonts.googleapis.com/css2?${params}&display=swap`;
}

function buildPackageJson(opts: ScaffoldOpts): string {
  const libraryDeps = LIBRARY_DEPS[opts.library] ?? LIBRARY_DEPS.shadcn;
  return JSON.stringify(
    {
      name: slugify(opts.businessName) || "stylus-variant",
      private: true,
      scripts: {
        dev: "next dev",
        build: "next build",
        start: "next start",
        lint: "next lint",
      },
      dependencies: {
        next: "14.2.5",
        react: "18.3.1",
        "react-dom": "18.3.1",
        ...libraryDeps,
      },
      devDependencies: {
        typescript: "5.5.3",
        "@types/node": "20.14.10",
        "@types/react": "18.3.3",
        "@types/react-dom": "18.3.0",
        tailwindcss: "3.4.6",
        postcss: "8.4.39",
        autoprefixer: "10.4.19",
      },
    },
    null,
    2,
  );
}

function buildLayout(opts: ScaffoldOpts): string {
  const fontLink = googleFontHref(opts.tokens);
  const linkTag = fontLink
    ? `\n        <link rel="preconnect" href="https://fonts.googleapis.com" />\n        <link rel="preconnect" href="https://fonts.gstatic.com" crossOrigin="anonymous" />\n        <link href="${fontLink}" rel="stylesheet" />`
    : "";

  return `import type { Metadata } from "next";
import "./globals.css";

export const metadata: Metadata = {
  title: ${JSON.stringify(opts.businessName)},
  description: ${JSON.stringify(`${opts.businessName} — ${opts.brief.primaryAction}`)},
};

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  return (
    <html lang="en">
      <head>${linkTag}
      </head>
      <body className="min-h-screen bg-[var(--color-background)] text-[var(--color-text)] font-[family-name:var(--font-body)] antialiased">
        {children}
      </body>
    </html>
  );
}
`;
}

function buildAgentsMd(opts: ScaffoldOpts): string {
  return `# AGENTS.md

## Stack
- Next.js 14 App Router
- React 18
- Tailwind CSS 3
- UI library: ${opts.library}

## Design tokens
Always read \`tokens.json\` before changing colors, typography, spacing, or radii.
Use CSS variables from \`app/globals.css\` (\`--color-*\`, \`--font-*\`, \`--spacing-*\`, \`--radius-*\`).
Do not hardcode hex values when a token exists.

## Archetype
${opts.brief.archetype} — ${opts.brief.voice}

## Motion
motionLevel: ${opts.brief.motionLevel}
`;
}

