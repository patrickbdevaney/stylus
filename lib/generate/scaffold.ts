export function buildBaseScaffold(opts: ScaffoldOpts): Record<string, string> {
  return {
    "package.json": buildPackageJson(opts),
    "tsconfig.json": JSON.stringify(
      {
        compilerOptions: {
          target: "ES2017",
          lib: ["dom", "dom.iterable", "esnext"],
          allowJs: false,
          skipLibCheck: true,
          strict: true,
          noEmit: true,
          esModuleInterop: true,
          module: "esnext",
          moduleResolution: "bundler",
          resolveJsonModule: true,
          isolatedModules: true,
          jsx: "preserve",
          incremental: true,
          paths: { "@/*": ["./*"] },
        },
        include: ["next-env.d.ts", "**/*.ts", "**/*.tsx"],
        exclude: ["node_modules"],
      },
      null,
      2,
    ),
    "tailwind.config.ts": tokensToTailwindTheme(opts.tokens, opts.library),
    "postcss.config.mjs": `export default {
  plugins: {
    tailwindcss: {},
    autoprefixer: {},
  },
};
`,
    "app/globals.css": `@tailwind base;
@tailwind components;
@tailwind utilities;

${tokensToCssVars(opts.tokens)}`,
    "app/layout.tsx": buildLayout(opts),
    "tokens.json": tokensToW3C(opts.tokens),
    "AGENTS.md": buildAgentsMd(opts),
    "README.md": buildReadme(opts),
  };
}
